                            </li>
                            <li>
                                Bebas memilih dan menentukan urutan sekolah yang diinginkan, maksimal 2 (dua) sekolah dari 40 SMP Negeri.
                            </li>
                            <li>
                                Nilai mata pelajaran/Nilai Akhir (NA) yang dipakai adalah Bahasa Indonesia, Matematika, dan IPA.
                            </li>
                            <li>
