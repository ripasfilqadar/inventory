                                    <li>
                                        Bahasa Indonesia;
                                    </li>
                                    <li>
                                        Matematika;
                                    </li>
                                    <li>
                                        IPA
                                    </li>
                                </ul>
                            </li>
                            <li>
                                Bagi peserta didik yang Kartu Keluarga (KK) dan domisilinya di wilayah Kabupaten Sidoarjo, tetapi asal sekolahnya dari luar Kabupaten Sidoarjo, tidak ada pembatasan kuota 10 % (persen).
                            </li>
                            <li>
                                Bagi peserta didik yang berdomisili di luar wilayah Kabupaten Sidoarjo dibatasi maksimal 10 % (persen) dari kuota Penerimaan Peserta Didik untuk masing-masing Satuan Pendidikan sebagaimana yang di amanatkan dalam Peraturan Bupati.
                            </li>
                            <li>
